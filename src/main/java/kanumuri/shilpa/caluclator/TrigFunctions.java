public class TrigFunctions {

    public double sin(double number){

        double sineOfANumber = Math.sin(number);
        return sineOfANumber ;
    }

    public double cos(double number){

        double cosOfANumber = Math.cos(number);
        return cosOfANumber;
    }

    public double tan(double number){

        double tanOfANumber = Math.tan(number);
        return tanOfANumber;
    }

    public double asin(double number){

        double arcsineOfANumber = Math.asin(number);
        return arcsineOfANumber ;
    }

    public double acos(double number){

        double arccosOfANumber = Math.acos(number);
        return arccosOfANumber;
    }

    public double atan(double number){

        double arctanOfANumber = Math.atan(number);
        return arctanOfANumber;
    }

}
